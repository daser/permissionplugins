'use strict';
//=============================================================================
/**
 * Module dependencies
 */
//=============================================================================
const
    mongoose = require('mongoose');
//=============================================================================
/**
 * RolesUsers Schema
 */
//=============================================================================
const RolesUsersSchema = mongoose.Schema({
    userId: {
        type: mongoose.Schema.ObjectId,
        required: true,
    },
    role: {
        type: String,
        required: true
    }
});
//=============================================================================
/**
 * Compile to Model
 */
//=============================================================================
const RolesUsersModel = mongoose.model('RolesUsers', RolesUsersSchema);
//=============================================================================
/**
 * Export RolesUsersModel
 */
//=============================================================================
module.exports = RolesUsersModel;
//=============================================================================
