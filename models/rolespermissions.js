'use strict';
//=============================================================================
/**
 * Module dependencies
 */
//=============================================================================
const
    mongoose = require('mongoose');
//=============================================================================
/**
 * RolesPermissions Schema
 */
//=============================================================================
const RolesPermissionsSchema = mongoose.Schema({
    role: {
        type: String,
        required: true
    },
    permissions:[String]
});
//=============================================================================
/**
 * Compile to Model
 */
//=============================================================================
const RolesPermissionsModel = mongoose.model('RolesPermissions', RolesPermissionsSchema);
//=============================================================================
/**
 * Export RolesPermissionsModel
 */
//=============================================================================
module.exports = RolesPermissionsModel;
//=============================================================================
